import UIKit

extension UIColor {
    
    enum AppColors: String {
        case mainColor = "MainColor"
        case textColor = "TextColor"
        case accentColor = "AccentColor"
        case appBackgroundColor = "AppBackgroundColor"
        case navigationBarColor = "NavigationBarColor"
    }
    
}

